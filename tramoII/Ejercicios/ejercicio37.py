'''
Escribir un programa que muestre todos los números enteros del 1 al 5, y luego los mismos números pero en orden inverso.
'''

# Mostrar números del 1 al 5 en orden ascendente
print("Números en orden ascendente:")
for i in range(1, 6):
    print(i)

# Mostrar números del 5 al 1 en orden descendente
print("Números en orden descendente:")
for i in range(5, 0, -1):
    print(i)
